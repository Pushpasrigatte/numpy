''' operations on arrays using numpy'''
import numpy as np
a=np.array([1,2,3,4])
b=np.array([5,6,7,8])
print("addition:",a+b)
print("product:",a+b)
print("square:",a**2)
print("sin values:",np.sin(a))
print("cos values:",np.cos(b))
print("tan values:",np.tan(a))
print("mean:",np.mean(a))
print("mean:",np.mean(b))
print("max:",np.max(a))
print("min:",np.min(b))
print("median:",np.median(b))

